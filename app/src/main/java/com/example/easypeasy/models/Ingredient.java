package com.example.easypeasy.models;

public class Ingredient {
    int id;
    float amount;
    String unit;
    String unitShort;
    String name;

    public int getId() {
        return id;
    }

    public float getAmount() {
        return amount;
    }

    public String getUnit() {
        return unit;
    }

    public String getUnitShort() {
        return unitShort;
    }

    public String getName() {
        return name;
    }
}
